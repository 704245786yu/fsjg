package com.basic.vo;

/**实力工厂,展示员工人数多的几个工厂*/
public class StrengthEnterpriseVo {

	private Integer id;
	private String name;
	private String logo;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	
}
